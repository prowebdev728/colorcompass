	<h5>Alberta Christmas Party</h5>
	<p>Color Compass kicked off the holiday season on Saturday, November 26th  with our first of two annual holiday parties at the beautiful Fairmont Hotel MacDonald in Downtown Edmonton. The evening was a great success with a turnout of 200 guests, our largest party to date! Among the attendees; a few of our generous manufacturers and sponsors; BASF, Akzo, Caruk &amp; Associates, Saint Gobain and 3M among others – We thank you for your contributions.</p>
	<p>Alongside dinner, dance, and the always entertaining photo booth, we also gave away some fantastic prizes, one of which was our grand prize, an LG 55" LED Smart TV – Congratulations to Don Guenther of Chase Winnipeg on your new TV!</p>
	<p>We also recognized 16 employee milestones in Alberta, which is a record for service recognitions in one year:</p>
