	<p>The Color Compass BC staff wrapped up 2016 with a fabulous annual holiday party on Saturday, December 10th at the Marriott Pinnacle Downtown Vancouver. As in Alberta, the Vancouver party had another record breaking 200 guests in attendance. Among those guests were our wonderful manufacturers and business partners; 3M, BASF, Akzo Nobel, Caruk &amp; Associates, DW Fontaine, RBC, and Miller Thomson. We thank them for their continued support and contributions.</p>
	<p>Not only did we enjoy a delicious plated dinner followed by dance and a (hilarious at times) holiday photo booth, we were also introduced to an evening of wonders by illusionist Vitaly Beckham. Vitaly managed to astonish even the most doubting audience member with his mind-bending magical acts. We still haven't figured out how he managed to make all of those drinks disappear!</p>
